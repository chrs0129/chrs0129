package day07_Operators;

public class ArithmeticOperators {
    public static void main(String[] args) {


        int num1 = 100;
        int num2 = 15;
        int num3 = num1 / num2;
        int num4 = num1 % num2; // contains remainder
        System.out.println("num3 = " + num3);
        System.out.println("num4 = " + num4);
        System.out.println(num1 + " devided by " + num2 + " is equal to " + num3 + " with remainder of " + num4);
    }




}
