package day03_EscapeSequences;


public class EscapeSequences {
    /*
    Escape sequences must be given in double quote
    \n starts a new line
    \t a new tab
    \\ it prints single backslash
    \" it prints double quote "
     */
    public static void main(String[] args) {
        System.out.println("java\nPython \nC#");
        System.out.println("------------------------------------------");
        System.out.println("Hello Cydeo\nHow are you all today?\nWhat class do we have next week. ");
        System.out.println("------------------------------------------");
        System.out.println("\tJava is Cool Programming Language");
        System.out.println("------------------------------------------");
        System.out.println("/ \\");
        System.out.println("------------------------------------------");
        System.out.println(" My favorit TV shoe is \"Game of Thrones\"");
// In order print double quote we need print \"


    }



}
